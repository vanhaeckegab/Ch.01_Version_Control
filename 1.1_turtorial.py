import turtle
ed = turtle.Turtle()
screen = turtle.Screen()  # makes a screen object
screen.setup(1000, 1000)

ed.pensize(50)  # width of pen line
ed.speed(10)  # speed of drawing. Go fast to not waste time.
ed.penup()
ed.goto(-500,100)
ed.color("red")
ed.pd()
ed.right(90)
ed.fd(200)
ed.right(180)
ed.fd(100)
ed.right(90)
ed.fd(100)
ed.right(90)
ed.fd(100)
ed.goto(-400,100)
ed.pu()
ed.color("purple")
ed.goto(-300,100)
ed.left(90)
ed.pd()
ed.fd(100)
ed.bk(100)
ed.right(90)
ed.fd(100)
ed.left(90)
ed.fd(100)
ed.bk(100)
ed.right(90)
ed.fd(100)
ed.left(90)
ed.fd(100)
ed.bk(100)
ed.pu()
ed.color("blue")
ed.goto(-100,100)
ed.pd()
ed.right(90)
ed.fd(200)
ed.left(90)
ed.fd(100)
ed.pu()
ed.color("purple")
ed.goto(100,100)
ed.pd()
ed.right(90)
ed.fd(200)
ed.left(90)
ed.fd(100)
ed.pu()
ed.color("orange")
ed.goto(350,-100)
ed.pd()
ed.circle(100)

turtle.exitonclick()  # Keeps pycharm window open so we can see the drawing
